    <dl>
        <dt id="faq-1">Do czego służy ta strona?</dt>
        <dd>Strona powstała aby ułatwić migrację kont XMPP z jednego serwera na drugi. Twój roster zostanie automatycznie skopiowany, a Twoi znajomi poinformowani. Oczywiście cały proces jest w pełni konfigurowalny, Ty decydujesz co przenosisz, skąd, gdzie i jak.</dd>
        <dt id="faq-2">Jakie warunki muszę spęłnić, aby móc przemigrować konto?</dt>
        <dd>Jedyne co musisz zrobić, aby móc przenieść swoje konto to samodzielne utworzenie konta na serwerze, na który chcesz przemigrować. Wynika to z braku możliwości łatwej implementacji rejestracji z kodami captha. Dodatkowo, nie możesz przemigrować konta na serwery facebooka czy nk, wynika to z ich zamkniętej specyfikacji.</dd>
        <dt id="faq-3">Znalazłem(am) błąd/mam propozycję, gdzie mogę to zgłosić?</dt>
        <dd>Jeżeli masz jakiekolwiek uwagi co do działania tej strony, nie krępuj się mnie o tym poinformować. Wszelkie potrzebne dane, wraz z formularzem znajdziesz na podstronie "kontakt"</dd>
        <dt id="faq-4">Czy moje hasła są bezpieczne?</dt>
        <dd>Oczywiście! W żadnym wypadku nie zapisujemy nigdzie Twojego hasła. Dane, które nam podajesz są od razu po migracji zapominane przez nas. Dodatkowo cały projekt jest 100% otwarty, a jego źródła można znaleźć <a href="https://github.com/kadet1090/xmpp-migrator">na GitHubie</a>.</dd>
        <dt id="faq-5">Czy projekt będzie nadal rozwijany?</dt>
        <dd>Sądzę, że doczeka się mniejszych lub większych aktualizacji w przyszłości. Jednak z pewnością nie będą to rewolucje, ma on tylko dobrze spełniać swoją rolę.</dd>
        <dt id="faq-6">Chciałbym przenieść również swoje transporty, czy to możliwe?</dt>
        <dd>Niestety, ze względu na specyfikę transportów przeniesienie ich jest po prostu niemożliwe. Niestety, będziesz je musiał zarejestrować samodzielnie na nowym serwerze.</dd>
        <dt id="faq-7">Kto jest autorem projektu?</dt>
        <dd>Projekt tworzę samodzielnie ja, czyli <a href="http://kadet.pipe-software.xaa.pl/">Kadet</a>. Jeżeli chciałbyś zobaczyć więcej moich projektów zapraszam <a href="http://kadet.pipe-software.xaa.pl/">na mojego bloga</a> :)</dd>
